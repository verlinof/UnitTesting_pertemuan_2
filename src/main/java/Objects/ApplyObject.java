package Objects;

import org.openqa.selenium.By;

public class ApplyObject {
    public By getPortofolioUrl() {
        return By.id("portofolioUrl");
    }

    public By getCvUrl() {
        return By.id("cvUrl");
    }

    public By getRole() {
        return By.id("role");
    }

    public By getSubmitBtn() {
        return By.id("btn-submit");
    }
}
